# Taqqoslash operatorlari

x = 5
y = 10

print("x == y:", x == y)    # False
print("x != y:", x != y)    # True
print("x > y:", x > y)      # False
print("x < y:", x < y)      # True
print("x >= 5:", x >= 5)    # True
print("y <= 5:", y <= 5)    # False






















