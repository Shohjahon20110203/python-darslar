# Arifmetik operatorlar
a = 10
b = 3

print("a + b =", a + b)      # 13
print("a - b =", a - b)      # 7
print("a * b =", a * b)      # 30
print("a / b =", a / b)      # 3.333...
print("a // b =", a // b)    # 3
print("a % b =", a % b)      # 1
print("a ** b =", a ** b)    # 1000






















